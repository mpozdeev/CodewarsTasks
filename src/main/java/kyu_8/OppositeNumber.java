package kyu_8;

/**
 * Instructions:
 * Very simple, given a number, find its opposite.
 * <p>
 * Examples: *
 * 1: -1
 * 14: -14
 * -34: 34
 */

public class OppositeNumber {
    public static int opposite(int number) {
        // your code here
        return number * (-1);
    }
}

// best practice: return -number;